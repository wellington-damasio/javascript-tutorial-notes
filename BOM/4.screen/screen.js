// The Screen object provides the attributes of the screen on which the current 
//window is being rendered.

// window.screen

// -----------------------------------------------
//              Some cool properties
// -----------------------------------------------

// - colorDepth: The number of bits used to represent colors
console.log(window.screen.colorDepth) // 24

// - height: Represent the pixel height of the screen
console.log(window.screen.height) // 1080

// - width: Represent the pixel width of the screen
console.log(window.screen.width) // 2560

// - availHeight: Represent the height of the screen minus system elements
console.log(window.screen.availHeight) //1036

// - availWidth: Represent the width of the screen minus system elements
console.log(window.screen.availWidth) // 2560
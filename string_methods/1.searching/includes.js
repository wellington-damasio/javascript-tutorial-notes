//The includes() method determines whether a string contains another string:

// strings.includes(searchString [, position])

// The includes() method returns true if the searchString found in the string; 
//otherwise false.

// -------------------------------------------------------------
//                  Checking for @ in an email
// -------------------------------------------------------------
let email = 'admin@example.com'
console.log(email.includes('@')) //true

let js = 'JavaScript'
console.log(js.includes('script')) // false (case-sensitive)





